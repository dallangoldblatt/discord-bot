    async def on_ready(self):
        print(f'Logged on as {self.user}!')

    async def on_message(self, message):
        # Ignore bot messages
        if message.author.bot:
            return

        content = message.content
        words = content.split()
        num_words = len(words)
        channel = message.channel

        # Handle -in command
        for word in words:
            if not word[-2:] == 'in':
                continue
            elif num_words == 1 or random.random() < 0.1:
                await channel.send(f'eyy I\'m {word} here')
                break

        # Randomly send weird chat
        if content == 'ayy lmao' or random.random() < 0.02:
            await channel.send(random.choice(self.random_words))


